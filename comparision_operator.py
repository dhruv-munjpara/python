num1=int(input("enter num1:"))
num2=int(input("enter num2:"))
print(num1>num2)  # greater than
print(num1<num2)  # less than
print(num1>=num2) # greater than equal to
print(num1<=num2) # less than equal to
print(num1==num2) # equal to
print(num1!=num2) # not equal to

print("-----------------------------------------------")

# comparision operator with formate
print(f"grater than{num1} > {num2} is {num1>num2}")
print(f"less than{num1} < {num2} is {num1<num2}")
print(f"greater than equal to{num1} >= {num2} is {num1>=num2}")
print(f"less than equal to{num1} <= {num2} is {num1<=num2}")
print(f"equal to{num1} == {num2} is {num1==num2}")
print(f" not equal to{num1} != {num2} is {num1!=num2}")
