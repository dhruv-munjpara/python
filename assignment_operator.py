#assignment operator
num=20
print(num)
num+=10
print("+= 10 is {}".format(num))
num-=10
print("-= 10 is {}".format(num))
num*=10 
print("*= 10 is {}".format(num))
num/=10
print("/= 10 is {}".format(num))
num//=10
print("//= 10 is {}".format(num))
num**=10
print("**= 10 is {}".format(num))
num%=10
print("%= 10 is {}".format(num))
